
public class Book {
	
	int id;
	String title;
	
	public Book(){
		
	}

	public Book(int id, String title){
		
	}

	//Getters
	public int getId() {
		return id;
	}

	
	public String getTitle() {
		return title;
	}

	//Setters
	public void setTitle(String title) {
		this.title = title;
	}
	
	public void setId(int id) {
		this.id = id;
	}

}
